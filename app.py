# Endpoints
# --------------------------------------------------------------------------- #
@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    body = request.get_json(silent=True) or {}
    text = (body.get("text") or "").strip()
    creator_id = body.get("creator_id")

    if not text:
        return jsonify({"error": "Field 'text' is required."}), 400
    if not creator_id:
        return jsonify({"error": "Field 'creator_id' is required."}), 400

    content_id = str(uuid.uuid4())
    result = score(text)
    ts = now_iso()

    conn = get_db()
    conn.execute(
        """INSERT INTO content
           (content_id, creator_id, text, attribution, confidence, p_ai,
            llm_score, style_score, status, created_at)
           VALUES (?,?,?,?,?,?,?,?,?,?)""",
        (
            content_id, creator_id, text, result["attribution"],
            result["confidence"], result["p_ai"], result["llm_score"],
            result["style_score"], "classified", ts,
        ),
    )
    conn.commit()
    conn.close()

    write_audit(
        {
            "content_id": content_id,
            "creator_id": creator_id,
            "timestamp": ts,
            "event": "classification",
            "attribution": result["attribution"],
            "confidence": result["confidence"],
            "p_ai": result["p_ai"],
            "llm_score": result["llm_score"],
            "style_score": result["style_score"],
            "signals_used": json.dumps(result["signals_used"]),
            "status": "classified",
            "appeal_reasoning": None,
        }
    )

    return jsonify(
        {
            "content_id": content_id,
            "creator_id": creator_id,
            "attribution": result["attribution"],
            "confidence": result["confidence"],
            "label": result["label"],
            "signals": {
                "groq_llm": result["llm_score"],
                "stylometry": result["style_score"],
                "style_metrics": result["style_metrics"],
                "combined_p_ai": result["p_ai"],
                "signals_used": result["signals_used"],
                "degraded": result["degraded"],
            },
            "status": "classified",
        }
    )


@app.route("/appeal", methods=["POST"])
def appeal():
    body = request.get_json(silent=True) or {}
    content_id = body.get("content_id")
    reasoning = (body.get("creator_reasoning") or "").strip()

    if not content_id:
        return jsonify({"error": "Field 'content_id' is required."}), 400
    if not reasoning:
        return jsonify({"error": "Field 'creator_reasoning' is required."}), 400

    conn = get_db()
    row = conn.execute(
        "SELECT * FROM content WHERE content_id = ?", (content_id,)
    ).fetchone()
    if row is None:
        conn.close()
        return jsonify({"error": "Unknown content_id."}), 404

    conn.execute(
        "UPDATE content SET status = ? WHERE content_id = ?",
        ("under_review", content_id),
    )
    conn.commit()
    conn.close()

    ts = now_iso()
    write_audit(
        {
            "content_id": content_id,
            "creator_id": row["creator_id"],
            "timestamp": ts,
            "event": "appeal",
            # Preserve the original decision alongside the appeal.
            "attribution": row["attribution"],
            "confidence": row["confidence"],
            "p_ai": row["p_ai"],
            "llm_score": row["llm_score"],
            "style_score": row["style_score"],
            "signals_used": None,
            "status": "under_review",
            "appeal_reasoning": reasoning,
        }
    )

    return jsonify(
        {
            "content_id": content_id,
            "status": "under_review",
            "message": "Appeal received. This content is now under human review.",
            "original_decision": {
                "attribution": row["attribution"],
                "confidence": row["confidence"],
            },
        }
    )


@app.route("/log", methods=["GET"])
def log():
    limit = request.args.get("limit", default=50, type=int)
    conn = get_db()
    rows = conn.execute(
        "SELECT * FROM audit_log ORDER BY id DESC LIMIT ?", (limit,)
    ).fetchall()
    conn.close()
    entries = []
    for r in rows:
        e = dict(r)
        if e.get("signals_used"):
            e["signals_used"] = json.loads(e["signals_used"])
        entries.append(e)
    return jsonify({"entries": entries})


@app.route("/", methods=["GET"])
def index():
    return jsonify(
        {
            "service": "Provenance Guard",
            "endpoints": ["POST /submit", "POST /appeal", "GET /log"],
        }
    )


init_db()

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=5000, debug=True)
